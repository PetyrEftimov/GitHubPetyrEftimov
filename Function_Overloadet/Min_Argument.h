#ifndef Min_Argument_h
#define Min_Argument_h



template <class Min>
 Min MinArgument(Min a , Min b) {
      if(a < b ){
          return a;
     }
      else{
          return b;
      }
        
   
}










#endif /* Min_Argument_h */
